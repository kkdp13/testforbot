def colornumber(color):
    if color == 'd':
        return 0
    elif color == 'e':
        return 1
    elif color == 'f':
        return 2
    elif color == 'g':
        return 3
    elif color == 'h':
        return 4
    elif color == 'i':
        return 5
    elif color == 'j':
        return 6
    elif color == 'k':
        return 7
    elif color == 'l':
        return 8
    else:
        return 9